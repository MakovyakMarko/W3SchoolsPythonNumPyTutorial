import numpy as np
# digits
arr = np.array([3,2,0,1])
x = np.sort(arr)
print(arr)
print(x)
# string
arr = np.array(['banana','cherry','apple'])
x = np.sort(arr)
print(arr)
print(x)
# boolean
arr = np.array([True, False, True])
x = np.sort(arr)
print(arr)
print(x)
# 2d array
arr = np.array([[3,2,4],[5,0,1]])
x = np.sort(arr)
print(arr)
print(x)